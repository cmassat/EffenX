    lda mFont, y
    sta $c180, y

    lda mFont + 255, y
    sta $c180 + 255, y

    lda mFont + 255 + 255, y
    sta $c180 + 255 + 255, y
    iny
    cpy #00
    bne _loop
     lda #0
    sta  MMU_IO_CTRL
    rts
.endsection

